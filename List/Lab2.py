# Create the List using the Range Function

myRangeList=list(range(0,10))
print(myRangeList)
print(type(myRangeList))


myRangeList1=list(range(0,20,3)) # Parameter 3 is called interval
print(myRangeList1)
print(type(myRangeList1))


myRangeList2=list(range(0,20,2)) # Parameter 3 is called interval
print(myRangeList2)
print(type(myRangeList2))
